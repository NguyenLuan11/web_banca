                        <p>
                            Ngoài ra, tùy vào số lượng cá và diện tích bể nuôi mà bạn nên thay nước thường xuyên, hạn chế để nước bị ô nhiễm hay thiếu oxi ảnh hưởng đến sức khỏe cá.
                            Lưu ý khi thay nước thì cần phải giữ lại 30 – 50% nước cũ để cá không bị sốc do chênh lệch pH quá nhiều.
                        </p>
