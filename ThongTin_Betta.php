                            Bên cạnh đó thì bạn chỉ nên cho cá ăn 2 lần/ngày, hạn chế bón thức ăn quá nhiều vì việc dư thừa thức ăn sẽ làm ô nhiễm nguồn nước và ảnh hưởng đến sức khỏe đàn cá.
