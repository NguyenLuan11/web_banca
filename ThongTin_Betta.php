                            </li>
                            <li>
                                <b>Lựa chọn cá giống và cho chúng vào hồ ép:</b> Con giống nên lựa chọn những con khỏe mạnh, có tuổi thọ từ 1 – 2 năm. Cho cá đực vào hồ trong khoảng 5 – 7 ngày,
                                khi thấy cá đực nhả bọt nhiều thì bắt đầu thả cá mái vào. Cá đực sẽ lùa và rỉa vây cá mái sau vài lần và xuất hiện trứng cá
                            </li>
                            <li>
                                <b>Vớt cá mái sau khi đẻ:</b> Cá mái sau khi dừng đẻ thì sẽ tiến hành vớt ra hồ riêng để tránh trường hợp cá mái nuốt trứng.
                            </li>
                            <li>
                                <b>Đợi trứng nở và vớt cá đực ra ngoài:</b> Khoảng từ 2 – 3 ngày thì trứng con sẽ nở, cá đực sẽ liên tục thay bong bóng bị vỡ do cá con chớm nở bám vào tổ ong.
                                Sau vài ngày khi cá con đã phát triển thì bạn nên tách cá đực khỏi đàn cá nhỏ để chúng sinh trưởng tiếp.
