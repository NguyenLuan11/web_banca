                        <h4>Hồ nuôi</h4>
                        <p>
                            Bể nuôi cá chuẩn cần đạt nhiệt độ từ 24 – 30 độ C và độ pH từ 7 – 7.5. Để đảm bảo cá có thể sinh trưởng và phát triển thì bạn nên thay nước từ 2 tuần/lần.
                            Lưu ý khi thay nước thì chỉ nên thay 2/3 lượng nước, giữ lại 1/3 lượng nước còn lại để cá không bị sốc với môi trường mới.
                        </p>
